package com.syntun.tools;

/**
 * 
 * 包装了Thread.sleep方法的类
 * 
 * @author tuo
 * 
 */
public class SleepUtil {
	/**
	 * 公共的sleep方法
	 * 
	 * @param time
	 *            睡眠时间毫秒
	 */
	public static void sleep(long time) {
		try {
			Thread.sleep(time);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
	}
}
