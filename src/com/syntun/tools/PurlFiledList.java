package com.syntun.tools;

/**
 * 
 * 拼接表中字段调用函数
 * 
 * @author tuo
 * 
 */
public class PurlFiledList {
	/**
	 * 平台id
	 */
	private String urlGroup;

	/**
	 * 拼接表生成的sort_id
	 */
	private String generateSortId;

	/**
	 * 要调用方法的字段
	 */
	private String fieldStr;

	/**
	 * 调用的方法
	 */
	private String filedFun;

	public String getUrlGroup() {
		return urlGroup;
	}

	public void setUrlGroup(String urlGroup) {
		this.urlGroup = urlGroup;
	}

	public String getGenerateSortId() {
		return generateSortId;
	}

	public void setGenerateSortId(String generateSortId) {
		this.generateSortId = generateSortId;
	}

	public String getFieldStr() {
		return fieldStr;
	}

	public void setFieldStr(String fieldStr) {
		this.fieldStr = fieldStr;
	}

	public String getFiledFun() {
		return filedFun;
	}

	public void setFiledFun(String filedFun) {
		this.filedFun = filedFun;
	}

}
